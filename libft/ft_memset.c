#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	size_t			i;
	unsigned char	*_b;

	i = 0;
	_b = b;
	while (i < n)
	{
		_b[i] = c;
		i++;
	}
	return (_b);
}
